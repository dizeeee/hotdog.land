<h2>What is Climb Talks?</h2>
<p>
	Climb Talks is a monthly event where people in the software and engineering communities listen to
	two lightning talks, and then climb and socialize. It's a great way to meet other people in the
	industry, learn something new, and get some exercise. People of all skill levels are welcome!
</p>

